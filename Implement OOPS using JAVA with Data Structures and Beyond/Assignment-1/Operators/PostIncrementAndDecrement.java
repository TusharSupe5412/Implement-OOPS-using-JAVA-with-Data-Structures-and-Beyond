package Operators;

public class PostIncrementAndDecrement {

	public static void main(String[] args) {
				//post increment
		
				int a=10;
				int b=a++;
				System.out.println(a);
				System.out.println(b);
				
				// post Decrement
				
				int a1=10;
				int b1=a1--;
				System.out.println(a1);
				System.out.println(b1);

	}

}
