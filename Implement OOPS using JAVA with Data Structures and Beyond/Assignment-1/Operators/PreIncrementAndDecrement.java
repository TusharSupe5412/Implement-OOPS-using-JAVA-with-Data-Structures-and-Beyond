package Operators;

public class PreIncrementAndDecrement 
{

	public static void main(String[] args)
	{
		//pre increment--First Value of a is increase and then assign to b
		
		int a=10;
		int b=++a;
		System.out.println(a);
		System.out.println(b);
		
		// pre Decrement--First Value of a is Decrease and then assign to b
		
		int a1=10;
		int b1=--a1;
		System.out.println(a1);
		System.out.println(b1);
			
	}
}
