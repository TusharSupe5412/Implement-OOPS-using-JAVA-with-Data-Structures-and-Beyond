package Operators;

public class IncrementAndDecrement {

	public static void main(String[] args) {
		int a= 10;
		System.out.println("Value entered is:"+ a);
		{
		a++;
		System.out.println("value of a is Increment to:"+ a);
		}
		
		{
			a--;
			System.out.println("Value of a is Decremented to:"+ a);
		}
		
		
	}

}
